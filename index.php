    <p>The human body contains around 8 to 10 pints of blood depending on the size of the individual. However, the composition of the blood is not the same in each person. This is what determines the person's blood type.</p>
    <p>There are four ABO groups:</p>
    <ol>
        <li>Group A: The surface of the red blood cells contains A antigen, and the plasma has anti-B antibody that would attack any foreign B antigen containing red blood cells.</li>
        <li>Group B: The surface of the red blood cells contains B antigen, and the plasma has anti-A antibody that would attack any foreign A antigen containing red blood cells.</li>
        <li>Group AB: The red blood cells have both A and B antigens, but the plasma does not contain anti-A/anti-B antibodies. Individuals with type AB can receive any ABO blood type.</li>
        <li>Group O: The plasma contains both types of anti-A/anti-B antibodies, but the surface of the red blood cells does not contain any A/B antigens. Having none of these A/B antigens means that they can be donated to a person with any ABO blood type.</li>
    </ol>

